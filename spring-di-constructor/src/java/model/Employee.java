package model;

/**
 *
 * @author developer
 */
public class Employee {
    private String name;
    private int age;

    public Employee(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
    
    public void show() {
        System.out.println("Name = " + name);
        System.out.println("Age = " + age);
    }
}
